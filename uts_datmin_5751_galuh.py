print("F1 Score  :", f1_score(y_test, y_pred_svm))
print("ROC AUC   :", roc_auc_score(y_test, y_prob_svm))

print("\n===== CLASSIFICATION REPORT SVM =====")
print(classification_report(y_test, y_pred_svm))

# =========================================================
